
-- Table to store dynamic lastmod dates for all SEO routes
CREATE TABLE public.page_metadata (
  path text PRIMARY KEY,
  updated_at timestamptz NOT NULL DEFAULT now(),
  changefreq text NOT NULL DEFAULT 'weekly',
  priority numeric NOT NULL DEFAULT 0.8,
  show_updated_date boolean NOT NULL DEFAULT true,
  created_at timestamptz NOT NULL DEFAULT now()
);

-- RLS
ALTER TABLE public.page_metadata ENABLE ROW LEVEL SECURITY;

-- Anyone can read (needed for AuthorMetaBar + sitemap edge function)
CREATE POLICY "Anyone can view page metadata"
  ON public.page_metadata FOR SELECT
  USING (true);

-- Only admins can update
CREATE POLICY "Admins can manage page metadata"
  ON public.page_metadata FOR ALL
  TO authenticated
  USING (public.has_role(auth.uid(), 'admin'))
  WITH CHECK (public.has_role(auth.uid(), 'admin'));

-- Service role can manage (for edge functions)
CREATE POLICY "Service role can manage page metadata"
  ON public.page_metadata FOR ALL
  USING (auth.role() = 'service_role')
  WITH CHECK (auth.role() = 'service_role');

